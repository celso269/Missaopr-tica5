numeros = [34, 7, 23, 32, 5, 62, 14, 19, 10, 43, 2, 55, 27, 3, 50]

numeros.sort()
print("Array ordenado de forma crescente:", numeros)

numeros.sort(reverse=True)
print("Array ordenado de forma decrescente:", numeros)

